#include "spoint.h"

//==============================================================================

namespace spcShiva
{
    
//==============================================================================
    
SPoint::SPoint() : x(0.0f), y(0.0f), z(0.0f)
{
}

//==============================================================================

SPoint::SPoint(const SFloat x, const SFloat y, const SFloat z) : x(x), y(y), z(z)
{
}

//==============================================================================

SPoint::SPoint(const SPoint& point)
{
    x = point.x;
    y = point.y;
    z = point.z;
}

//==============================================================================

SPoint::~SPoint()
{
}

//==============================================================================
    
}   //  namespace spcShiva
