#include "spoint2d.h"

//==============================================================================

namespace spcShiva
{
    
//==============================================================================
    
SPoint2D::SPoint2D() : x(0.0f), y(0.0f)
{
}

//==============================================================================

SPoint2D::SPoint2D(const SFloat x, const SFloat y) : x(x), y(y)
{
}

//==============================================================================

SPoint2D::SPoint2D(const SPoint2D& point)
{
    x = point.x;
    y = point.y;
}

//==============================================================================

SPoint2D::~SPoint2D()
{
}

//==============================================================================
    
}   //  namespace spcShiva
